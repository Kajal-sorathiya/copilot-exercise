import UIKit

class Calculations {
    private func calculateAdditionFor(_ number1: Int, with number2: Int) {
        let result = number1 + number2
        print("The result of \(number1) + \(number2) is \(result)")
    }

    private func calculateSubtractionFor(_ number1: Int, with number2: Int) {
        let result = number1 - number2
        print("The result of \(number1) - \(number2) is \(result)")
    }
}